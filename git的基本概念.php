repository    仓库    用来存放代码  每个项目对应一个仓库
star          收藏    将喜欢的内容收藏起来 方便下次游览
fork          复制    把别人的仓库复制一份给自己 是独立存在的  在仓库名字下面会有 forked from 某某某  说明从谁那里fork的
pull request  发出一种请求  我把代码写好希望和原来你给我的仓库合并 我给你发个合并请求  你看完之后觉得好就同意合并
watch         关注    把喜欢的内容关注  别人一有动态就通知你
issue         发现问题要讨论时候用的   比方软件有bug 我给你发了一个issue说明问题 你会看到有消息通知issue 咱俩进行讨论 完事谁都可以关闭 能切换状态
github主页    显示我的动态和所有git库  单击小猫就是回到主页
仓库主页       单击某个仓库就会进入仓库主页  里面可以对仓库文件进行添加修改删除  都是单击文件名实现的 每次操作必须写描述  
个人主页       显示我的个人一些信息等
